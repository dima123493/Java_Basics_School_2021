package com.dataart.school.lessons.Lecture2;

public class OperatorsExample {
    public static void main(String[] args) {

        int a=7;
        int b=2;
        System.out.println(a/b);
        System.out.println(a%b);

        int c=7;
        System.out.println(c++);
        System.out.println(c);
        System.out.println(++c);

        int x=1;
        x=x+10;
        x+=10;
        System.out.println(x);
    }
}
